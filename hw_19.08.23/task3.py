first_number = 10
second_number = 2

# Addition
print(first_number + second_number)
print()
# Subtraction
print(first_number - second_number)
print()
# Division
print(first_number / second_number)
print()
# Multiplication
print(first_number * second_number)
print()
# Exponent (Power)
print(first_number ** second_number)
print()
# Modulus
print(first_number % second_number)
print()
# Floor division
print(first_number // second_number)
